# Mix letters and numbers

a = 1
b = '3'
# Convert a to a string
puts a.to_s + b
puts 'hello'.to_s
# Convert b to an interger
puts a + b.to_i
puts '7 is my favourte number'.to_i
# Convert b to a float
puts a + b.to_f
puts 'Yo mama!'.to_f

